interface Icon3DProps {
  name: string;
  size?: "sm" | "md" | "lg" | "xl";
  flat?: boolean;
  className?: string;
}

export function Icon3D({ name, size = "md", flat = false, className }: Icon3DProps) {
  const sizeMap = {
    sm: { container: 32, icon: 16, radius: 8, padding: 8 },
    md: { container: 40, icon: 20, radius: 10, padding: 10 },
    lg: { container: 48, icon: 24, radius: 12, padding: 12 },
    xl: { container: 56, icon: 28, radius: 14, padding: 14 },
  };

  const s = sizeMap[size];
  const gradient = ICON_GRADIENTS[name] || { from: "#007AFF", to: "#5856D6" };
  const svgContent = ICON_PATHS[name];

  if (!svgContent) return null;

  if (flat) {
    return (
      <div
        className={cn("flex items-center justify-center shrink-0", className)}
        style={{ width: s.icon, height: s.icon }}
      >
        <div style={{ width: s.icon, height: s.icon, color: gradient.from }}>
          {svgContent}
        </div>
      </div>
    );
  }

  return (
    <div
      className={cn("shrink-0", className)}
      style={{
        width: s.container,
        height: s.container,
        perspective: "500px",
      }}
    >
      <div
        className="flex items-center justify-center text-white shadow-[var(--shadow-md)]"
        style={{
          width: s.container,
          height: s.container,
          borderRadius: s.radius,
          padding: s.padding,
          background: `linear-gradient(135deg, ${gradient.from}, ${gradient.to})`,
          transform: "rotateX(5deg) rotateY(-5deg)",
          transformStyle: "preserve-3d",
        }}
      >
        <div style={{ width: s.icon, height: s.icon }}>
          {svgContent}
        </div>
      </div>
    </div>
  );
}

